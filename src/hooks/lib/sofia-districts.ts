export function getDistrictFromCoordinates(
  lat: number,
  lng: number
): string | undefined {
  for (const district of SOFIA_DISTRICTS) {
    const { minLat, maxLat, minLng, maxLng } = district.bounds;
    if (lat >= minLat && lat <= maxLat && lng >= minLng && lng <= maxLng) {
      return district.name;
    }
  }
  return undefined;
}

export function getDistrictById(id: number) {
  return SOFIA_DISTRICTS.find((d) => d.id === id);
}

export function getAllDistrictNames(): string[] {
  return SOFIA_DISTRICTS.map((d) => d.name);
}

export function isInSofia(lat: number, lng: number): boolean {
  return lat >= 42.6 && lat <= 42.8 && lng >= 23.1 && lng <= 23.5;
}
